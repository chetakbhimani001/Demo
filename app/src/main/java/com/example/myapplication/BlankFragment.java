    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
        getUserList();
        setUIRef(view);
    }
    private void setUIRef(View view)
    {
        RecyclerView recyclerView = view.findViewById(R.id.myRecyclerView);

        MyRecyclerViewAdapter myRecyclerViewAdapter = new MyRecyclerViewAdapter(mUserData.getUsers());

        recyclerView.setAdapter(myRecyclerViewAdapter);

        LinearLayoutManager linearLayoutManager = new LinearLayoutManager(requireActivity(), RecyclerView.VERTICAL, false);
        recyclerView.setLayoutManager(linearLayoutManager);
    }

    private void getUserList()
    {
        AssetManager assetManager = getActivity().getAssets();
        try
        {
            InputStream inputStream = assetManager.open("user_data.json");

            Gson gson = new Gson();

            Reader reader = new InputStreamReader(inputStream);

            mUserData = gson.fromJson(reader, UserData.class);

        } catch (IOException e)
        {
            e.printStackTrace();
        }
    }
